                        <h2 class="text-center">THE HISTORY OF <span class="text-theme-color-2">FILM MAKING</span></h2>

                       <p>Filmmaking is the process of making a film and also the best way to tell your story. Filmmaking involves a number of discrete stages including an initial story, idea, or commission, through screenwriting, casting, shooting, sound recording and reproduction, editing, and screening the finished product before an audience that may result in a film release and exhibition. Filmmaking is an exciting job and is a lot of fun. But how exactly did the filmmaking industry evolve and become what it is today? The History of filmmaking goes back till the late of 19th century. In the year 1872, Eadweard Muybridge began experimenting on capturing moving images. He placed twelve cameras on a race horse track, spread thread across the track, and attached the thread into contact with a camera's shutter. Once the horse ran across the track, its legs broke the threads, causing the cameras to operate in sequence. The ending results were 12 photos showing a horse's gait. With an invention of his called the Zoopraxiscope, he was able to quickly project these images, creating what is known as motion photography and the first movie to ever exist.</p>

                 

