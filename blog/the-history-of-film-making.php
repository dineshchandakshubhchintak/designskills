                     <p>The first film for motion photography was invented in 1885 by George Eastman and William H. Walker, which contributed to the advance of motion photography. The 1900’s were a time of great advancement for film and motion picture technology. It was the time when editing, backdrops, and visual flow started being discovered. The first use of animation in movies was in 1899; with the production of the short film Matches: An Appeal by British film pioneer Arthur Melbourne.</p>

                  <p>Around 1905 ‘Nickelodeon’ started screening movies to the public at an affordable and inexpensive price. The 1920s were the time when the movie industry truly began to flourish with movies being made continuously. And the Hollywood was the birthplace of movies. The earliest and most affluent film companies were Warner Brothers Pictures, Paramount, RKO, Metro Goldwin Mayer, and 20th Century Fox, each of whom owned their own film production sets and studios.</p>

                  <p>1930’s were the era when sound came in to the picture. After the introduction of sound, new genres of movies like action, romance, thriller, musicals, comedies, horror began to be explored.

                    <br> It was after the 1970’s that VHS video players, laser disc players, and films on videocassette tapes and discs were discovered, which greatly increased profits and revenue for studios.</p>

                  <p>The 2000’s have been an era of immense change in the movie and technology industries, and more change is sure to come. There has been an increasing globalization of cinema during this decade, with foreign-language films gaining popularity in English-speaking markets. After James Cameron's 3D film Avatar became the highest-grossing film of all time, 3D films have gained increasing popularity with many other films being released in 3D, with the best critical and financial successes being in the field of feature film animation. As of 2010, the largest film industries by number of feature films produced are those of India, the United States, China, Nigeria and Japan. </p>

