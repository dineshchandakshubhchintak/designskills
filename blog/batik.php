                  <h4 class="mb-30 line-height-1 text-uppercase text-left">TECHNIQUE<span class="text-theme-color-2">   </span></h4>

                  <p>Firstly, a cloth is washed, soaked and beaten with a large mallet. Designs are drawn with pencil and later redrawn using hot wax, usually made from a mixture of paraffin or bees wax, sometimes mixed with plant resins, which functions as a dye-resist. The wax can be applied with a variety of tools. A pen-like instrument called a canting is the most common. A tjanting is made from a small copper reservoir with a spout on a wooden handle. The reservoir holds the resist which flows through the spout, creating dots and lines as it moves. For larger patterns, a stiff brush may be used. Alternatively, a copper block stamp called a cap (is used to cover large areas more efficiently.

                  </p>

                  <p>e resist is removed by boiling or scraping the cloth. The areas treated with resist keep their original color; when the resist is removed the contrast between the dyed and undyed areas forms the pattern. This process is repeated as many times as the number of colors desired. The most traditional type of batik, called batik tulis (written batik), is drawn using only the canting. The cloth need to be drawn on both sides and dipped in a dye bath three to four times. The whole process may take up to a year; it yields considerably finer patterns than stamped batik.

