                  <p>

                     To make batik, selected areas of the cloth are blocked out by brushing or drawing hot wax over them, and the cloth is then dyed. The parts covered in wax resist the dye and remain the original color. This process of waxing and dyeing can be repeated to create more elaborate and colorful designs. After the final dyeing the wax is removed and the cloth is ready for wearing or showing.

                    <br> The present Batik is different from how it used to be done in the past. The artist may use etching, discharge dyeing, stencils, different tools for waxing and dyeing, wax recipes with different resist values and work with silk, cotton, wool, leather, paper or even wood and ceramics.

