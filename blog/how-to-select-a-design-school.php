                         <h4 class="mb-30 line-height-1 text-uppercase text-left">Location :<span class="text-theme-color-2">   </span></h4>

                         <p>This is of utmost importance that the institute or design school you wish to attend is in close proximity to where you are currently residing or is easily accessible to the main road, has good public transportation facilities, industries pertaining to your field of study and design studio's are close by and is centrally located. </p>

                        <br>

                         <h4 class="mb-30 line-height-1 text-uppercase text-left">Curriculum :<span class="text-theme-color-2">   </span></h4>

                        

                         <p>Most institutes do offer customized content based on your budget and time. You have to understand when you have to master the skills you need to understand the subject, imbibe, put in practice and work on project. Therefore, make sure the institute you choose has industry experienced people. The subject covering fundamentals, objective and attention to detail  and covers most areas which is currently in practice in the industry. It gives you confident that the course structure is in accordance to the industry standards. </p>

                          <br>

                          <h4 class="mb-30 line-height-1 text-uppercase text-left">Faculty :<span class="text-theme-color-2">   </span></h4>

                          <p>Have a word with the respective faculty in the design school or the person in charge of the school. get to know their expertise and experience in the field, their teaching methodology, get feedback from the alumni of the design school. </p>

                           <br>

                          <h4 class="mb-30 line-height-1 text-uppercase text-left">Fee <span class="text-theme-color-2"> structure:  </span></h4>

                          <p>This goes without saying that the fee structure should obviously make sense to you and your spending capacity. Make sure the fees are in par with the curriculum & facilities offered by the design school. There are various options available to students to pay their fee's ranging from student loans, scholarships to monthly installment facilities. Check if the Design School you have set your heart on provides you with the same. At the same time there are various design school offer courses based on your budget. Sincere advice would be don’t choose the course based on the fee. </p>

                           <br>

                          <h4 class="mb-30 line-height-1 text-uppercase text-left">Amenities :<span class="text-theme-color-2">   </span></h4>

                          <p>It can not be stressed enough that design schools of good repute will have excellent infrastructure, good hygiene & sanitation facilities and will be equipped with state of art technologies as well as good amenities for the students. </p>

                           <br>

                          <h4 class="mb-30 line-height-1 text-uppercase text-left">Reputation :<span class="text-theme-color-2">   </span></h4>

                          <p>There are very few renowned design schools in India, make sure you get all your facts right, do a good background search, find out about the courses offered and how good the design school is from existing students, understand the object of the promoter, talk to the faculty or experts present at the institute , browse the internet and then enroll for the course that you want to study. </p>

                           <br>

